# Thresholds for cost/latency spike warnings
COST_SPIKE_THRESHOLD = 0.5  # 50% increase triggers warning
LATENCY_SPIKE_THRESHOLD = 0.5  # 50% increase triggers warning

# Max retries for gh CLI calls
GH_MAX_RETRIES = 3
GH_BASE_DELAY = 1.0  # seconds


def load_latest_results(results_dir: str = ".evalview/results") -> Optional[Dict[str, Any]]:
    """Load the most recent results file."""
    results_path = Path(results_dir)
    if not results_path.exists():
        return None

    json_files = sorted(
        results_path.glob("*.json"),
        key=lambda x: x.stat().st_mtime,
        reverse=True
    )
    if not json_files:
        return None

    try:
        with open(json_files[0], encoding="utf-8") as f:
            return json.load(f)
    except (json.JSONDecodeError, IOError):
        return None


def get_status_display(status: str) -> Dict[str, str]:
    """Get display configuration for a status."""
    return STATUS_CONFIG.get(status.lower(), DEFAULT_STATUS)


def format_cost(cost: float) -> str:
    """Format cost value for display."""
    if cost == 0:
        return "$0.00"
    elif cost < 0.01:
        return f"${cost:.4f}"
    return f"${cost:.2f}"


def format_latency(ms: float) -> str:
    """Format latency in human-readable form."""
    if ms < 1000:
        return f"{ms:.0f}ms"
    return f"{ms/1000:.1f}s"


def _extract_pr_number() -> Optional[int]:
    """Extract PR number from GitHub Actions environment."""
    github_ref = os.environ.get("GITHUB_REF", "")
    if "/pull/" not in github_ref:
        return None

    try:
        return int(github_ref.split("/pull/")[1].split("/")[0])
    except (IndexError, ValueError):
        return None


def _get_run_url() -> Optional[str]:
    """Get URL to the current GitHub Actions run."""
    server = os.environ.get("GITHUB_SERVER_URL", "https://github.com")
    repo = os.environ.get("GITHUB_REPOSITORY", "")
    run_id = os.environ.get("GITHUB_RUN_ID", "")

    if repo and run_id:
        return f"{server}/{repo}/actions/runs/{run_id}"
    return None


def _run_gh_command(
    args: List[str],
    timeout: int = 30,
    max_retries: int = GH_MAX_RETRIES,
) -> subprocess.CompletedProcess:
    """Run a gh CLI command with exponential backoff retry.

    Retries on transient failures (timeouts, server errors).
    Does NOT retry on auth failures or missing CLI.

    Raises:
        subprocess.CalledProcessError: After all retries exhausted
        FileNotFoundError: gh CLI not installed (no retry)
    """
    last_error: Optional[Exception] = None
    for attempt in range(max_retries):
        try:
            result = subprocess.run(
                ["gh"] + args,
                capture_output=True,
                text=True,
                check=True,
                timeout=timeout,
            )
            return result
        except FileNotFoundError:
            raise  # gh not installed — no point retrying
        except subprocess.TimeoutExpired as e:
            last_error = e
            if attempt < max_retries - 1:
                delay = GH_BASE_DELAY * (2 ** attempt)
                logger.debug("gh command timed out, retrying in %.1fs (attempt %d/%d)",
                             delay, attempt + 1, max_retries)
                time.sleep(delay)
        except subprocess.CalledProcessError as e:
            last_error = e
            stderr = e.stderr or ""
            # Don't retry auth failures or 404s
            if "401" in stderr or "403" in stderr or "404" in stderr:
                raise
            if attempt < max_retries - 1:
                delay = GH_BASE_DELAY * (2 ** attempt)
                logger.debug("gh command failed, retrying in %.1fs (attempt %d/%d)",
                             delay, attempt + 1, max_retries)
                time.sleep(delay)

    # All retries exhausted
    if last_error is not None:
        raise last_error
    raise RuntimeError("gh command failed after retries")


def _determine_overall_status(
    results: List[Dict[str, Any]],
    diff_results: Optional[List[Dict[str, Any]]] = None
) -> str:
    """Determine overall status from results."""
    if diff_results:
        statuses = [d.get("overall_severity", "passed") for d in diff_results]
        if "regression" in statuses:
            return "regression"
        if "tools_changed" in statuses:
            return "tools_changed"
        if "output_changed" in statuses:
            return "output_changed"
        return "passed"

    failed = sum(1 for r in results if not r.get("passed", False))
    return "passed" if failed == 0 else "regression"


# ── Cost / latency spike detection ──────────────────────────────────────


def _detect_cost_spike(
    check_data: Dict[str, Any],
) -> Optional[Tuple[float, float]]:
    """Detect cost spike from check data.

    Returns (current_cost, baseline_cost) if spike detected, else None.
    """
    diffs = check_data.get("diffs", [])
    if not diffs:
        return None

    total_current = 0.0
    total_baseline = 0.0
    has_cost_data = False

    for d in diffs:
        current_cost = d.get("current_cost", 0)
        baseline_cost = d.get("baseline_cost", 0)
        if current_cost > 0 or baseline_cost > 0:
            has_cost_data = True
            total_current += current_cost
            total_baseline += baseline_cost

    if not has_cost_data or total_baseline == 0:
        return None

    increase = (total_current - total_baseline) / total_baseline
    if increase > COST_SPIKE_THRESHOLD:
        return (total_current, total_baseline)
    return None


def _detect_latency_spike(
    check_data: Dict[str, Any],
) -> Optional[Tuple[float, float]]:
    """Detect latency spike from check data.

    Returns (current_latency_ms, baseline_latency_ms) if spike detected, else None.
    """
    diffs = check_data.get("diffs", [])
    if not diffs:
        return None

    total_current = 0.0
    total_baseline = 0.0
    has_latency_data = False

    for d in diffs:
        current_latency = d.get("current_latency", 0)
        baseline_latency = d.get("baseline_latency", 0)
        if current_latency > 0 or baseline_latency > 0:
            has_latency_data = True
            total_current += current_latency
            total_baseline += baseline_latency

    if not has_latency_data or total_baseline == 0:
        return None

    increase = (total_current - total_baseline) / total_baseline
    if increase > LATENCY_SPIKE_THRESHOLD:
        return (total_current, total_baseline)
    return None


def _detect_model_change(check_data: Dict[str, Any]) -> Optional[str]:
    """Detect model version change from check data.

    Returns description string if model changed, else None.
    """
    summary = check_data.get("summary", {})
    if summary.get("model_changed"):
        # Try to extract old/new model names from diffs
        for d in check_data.get("diffs", []):
            old_model = d.get("baseline_model", "")
            new_model = d.get("current_model", "")
            if old_model and new_model and old_model != new_model:
                return f"{old_model} \u2192 {new_model}"
        return "Yes"
    return None


# ── Summary table builders ──────────────────────────────────────────────


def _build_summary_table(results: List[Dict[str, Any]]) -> List[str]:
    """Build the summary metrics table."""
    total = len(results)
    passed = sum(1 for r in results if r.get("passed", False))
    pass_rate = (passed / total * 100) if total > 0 else 0

    total_cost = sum(
        r.get("trace", {}).get("metrics", {}).get("total_cost", 0)
        for r in results
    )
    total_latency = sum(
        r.get("trace", {}).get("metrics", {}).get("total_latency", 0)
        for r in results
    )
    avg_score = sum(r.get("score", 0) for r in results) / total if total > 0 else 0

    return [
        "| Metric | Value |",
        "|--------|-------|",
        f"| Tests | {passed}/{total} passed ({pass_rate:.0f}%) |",
        f"| Avg Score | {avg_score:.1f}/100 |",
        f"| Total Cost | {format_cost(total_cost)} |",
        f"| Total Latency | {format_latency(total_latency)} |",
    ]


def _build_changes_section(diff_results: List[Dict[str, Any]], max_items: int = 5) -> List[str]:
    """Build the changes from baseline section with collapsible overflow."""
    changes = [d for d in diff_results if d.get("has_differences", False)]
    if not changes:
        return []

    lines = ["### Changes from Baseline", ""]

    for diff in changes[:max_items]:
        test_name = diff.get("test_name", "Unknown")
        status = diff.get("overall_severity", "passed")
        status_display = get_status_display(status)

        summary_parts = []

        score_diff = diff.get("score_diff", 0)
        if abs(score_diff) > 1:
            sign = "+" if score_diff > 0 else ""
            summary_parts.append(f"score {sign}{score_diff:.1f}")

        tool_diffs = diff.get("tool_diffs", [])
        if tool_diffs:
            summary_parts.append(f"{len(tool_diffs)} tool change(s)")

        latency_diff = diff.get("latency_diff", 0)
        if abs(latency_diff) > 100:
            sign = "+" if latency_diff > 0 else ""
            summary_parts.append(f"latency {sign}{latency_diff:.0f}ms")

        summary = ", ".join(summary_parts) if summary_parts else "minor changes"
        lines.append(f"- {status_display['emoji']} **{test_name}**: {summary}")

    remaining = len(changes) - max_items
    if remaining > 0:
        lines.append("")
        lines.append("<details>")
        lines.append(f"<summary>... and {remaining} more change(s)</summary>")
        lines.append("")
        for diff in changes[max_items:]:
            test_name = diff.get("test_name", "Unknown")
            status = diff.get("overall_severity", "passed")
            status_display = get_status_display(status)
            lines.append(f"- {status_display['emoji']} **{test_name}**")
        lines.append("")
        lines.append("</details>")

    lines.append("")
    return lines


def _build_failed_tests_section(results: List[Dict[str, Any]], max_items: int = 5) -> List[str]:
    """Build the failed tests section with collapsible overflow."""
    failed_tests = [r for r in results if not r.get("passed", False)]
    if not failed_tests:
        return []

    lines = ["### Failed Tests", ""]

    for r in failed_tests[:max_items]:
        name = r.get("test_case", "Unknown")
        score = r.get("score", 0)
        min_score = r.get("min_score", 70)
        lines.append(f"- \u274c **{name}**: score {score:.1f} (min: {min_score})")

    remaining = len(failed_tests) - max_items
    if remaining > 0:
        lines.append("")
        lines.append("<details>")
        lines.append(f"<summary>... and {remaining} more failure(s)</summary>")
        lines.append("")
        for r in failed_tests[max_items:]:
            name = r.get("test_case", "Unknown")
            score = r.get("score", 0)
            lines.append(f"- \u274c **{name}**: score {score:.1f}")
        lines.append("")
        lines.append("</details>")

    lines.append("")
    return lines


def _build_check_summary_table(
    check_data: Dict[str, Any],
    model_change: Optional[str] = None,
) -> List[str]:
    """Build summary table from check --json output format."""
    summary = check_data.get("summary", {})
    total = summary.get("total_tests", 0)
    unchanged = summary.get("unchanged", 0)
    regressions = summary.get("regressions", 0)
    tools_changed = summary.get("tools_changed", 0)
    output_changed = summary.get("output_changed", 0)
    pass_rate = (unchanged / total * 100) if total > 0 else 0

    lines = [
        "| Metric | Value |",
        "|--------|-------|",
        f"| Tests | {unchanged}/{total} unchanged ({pass_rate:.0f}%) |",
    ]
    if regressions:
        lines.append(f"| Regressions | {regressions} |")
    if tools_changed:
        lines.append(f"| Tools Changed | {tools_changed} |")
    if output_changed:
        lines.append(f"| Output Changed | {output_changed} |")

    # Model change callout
    if model_change is None:
        model_change = _detect_model_change(check_data)
    if model_change:
        lines.append(f"| Model Changed | {model_change} |")

    return lines


def _build_check_changes_section(check_data: Dict[str, Any], max_items: int = 5) -> List[str]:
    """Build changes section from check --json with collapsible overflow."""
    diffs = check_data.get("diffs", [])
    changes = [d for d in diffs if d.get("status") != "passed"]
    if not changes:
        return []

    lines = ["### Changes from Baseline", ""]

    for diff in changes[:max_items]:
        test_name = diff.get("test_name", "Unknown")
        status = diff.get("status", "passed")
        status_display = get_status_display(status)

        summary_parts = []

        score_delta = diff.get("score_delta", 0)
        if abs(score_delta) > 1:
            sign = "+" if score_delta > 0 else ""
            summary_parts.append(f"score {sign}{score_delta:.1f}")

        tool_diffs = diff.get("tool_diffs", [])
        if tool_diffs:
            summary_parts.append(f"{len(tool_diffs)} tool change(s)")

        sim = diff.get("output_similarity", 1.0)
        if sim < 0.95:
            summary_parts.append(f"output {sim:.0%} similar")

        # Append turn-level detail when available
        turn_diffs = diff.get("turn_diffs") or []
        failed_turns = [td for td in turn_diffs if td.get("status") != "passed"]
        if failed_turns:
            turn_parts = []
            for td in failed_turns:
                turn_idx = td.get("turn", td.get("turn_index", "?"))
                td_status = td.get("status", "changed").replace("_", " ")
                output_sim = td.get("output_similarity")
                part = f"Turn {turn_idx} {td_status}"
                if output_sim is not None and output_sim < 0.95:
                    part += f" ({int(output_sim * 100)}% similar)"
                turn_parts.append(part)
            summary_parts.extend(turn_parts)

        summary = ", ".join(summary_parts) if summary_parts else "minor changes"
        lines.append(f"- {status_display['emoji']} **{test_name}**: {summary}")

    remaining = len(changes) - max_items
    if remaining > 0:
        lines.append("")
        lines.append("<details>")
        lines.append(f"<summary>... and {remaining} more change(s)</summary>")
        lines.append("")
        for diff in changes[max_items:]:
            test_name = diff.get("test_name", "Unknown")
            status = diff.get("status", "passed")
            status_display = get_status_display(status)
            lines.append(f"- {status_display['emoji']} **{test_name}**")
        lines.append("")
        lines.append("</details>")

    lines.append("")
    return lines


# ── Alerts section (cost/latency spikes, model changes) ─────────────────


def _build_alerts_section(
    check_data: Dict[str, Any],
    model_change: Optional[str] = None,
) -> List[str]:
    """Build alerts section for cost spikes, latency spikes, model changes."""
    alerts: List[str] = []

    # Cost spike
    cost_spike = _detect_cost_spike(check_data)
    if cost_spike:
        current, baseline = cost_spike
        pct = ((current - baseline) / baseline) * 100
        alerts.append(
            f"- \U0001f4b8 **Cost spike**: {format_cost(baseline)} \u2192 "
            f"{format_cost(current)} (+{pct:.0f}%)"
        )

    # Latency spike
    latency_spike = _detect_latency_spike(check_data)
    if latency_spike:
        current, baseline = latency_spike
        pct = ((current - baseline) / baseline) * 100
        alerts.append(
            f"- \u23f1\ufe0f **Latency spike**: {format_latency(baseline)} \u2192 "
            f"{format_latency(current)} (+{pct:.0f}%)"
        )

    # Model change (standalone alert, not just a table row)
    if model_change is None:
        model_change = _detect_model_change(check_data)
    if model_change and model_change != "Yes":
        alerts.append(f"- \U0001f916 **Model changed**: {model_change}")

    if not alerts:
        return []

    lines = ["> **Alerts**", ">"]
    for alert in alerts:
        lines.append(f"> {alert}")
    lines.append("")
    return lines


# ── Generate report builders ────────────────────────────────────────────


def _build_generate_summary_table(report: Dict[str, Any]) -> List[str]:
    """Build summary table from generate report format."""
    covered = report.get("covered", {})
    discovery = report.get("discovery", {})
    return [
        "| Metric | Value |",
        "|--------|-------|",
        f"| Source | {report.get('source', 'live probing')} |",
        f"| Draft Tests | {report.get('tests_generated', 0)} |",
        f"| Probe Runs | {report.get('probes_run', 0)} |",
        f"| Discovered Tools | {discovery.get('count', 0)} |",
        f"| Tool Paths | {covered.get('tool_paths', 0)} |",
        f"| Multi-turn Paths | {covered.get('multi_turn', 0)} |",
    ]


def _build_generate_discovery_section(report: Dict[str, Any], max_items: int = 6) -> List[str]:
    """Build discovery and behavior summary for generated suites."""
    lines: List[str] = []
    discovery = report.get("discovery", {})
    tools = discovery.get("tools", [])
    draft_tests = report.get("draft_tests", [])

    if tools:
        lines.extend(["### Discovered Tools", ""])
        for tool in tools[:max_items]:
            description = tool.get("description", "")
            suffix = f": {description[:80]}" if description else ""
            lines.append(f"- `{tool.get('name', 'unknown')}`{suffix}")
        remaining = len(tools) - max_items
        if remaining > 0:
            lines.append(f"- ... and {remaining} more")
        lines.append("")

    if draft_tests:
        lines.extend(["### Draft Behaviors", ""])
        for item in draft_tests[:max_items]:
            lines.append(
                f"- **{item.get('name', 'Unknown')}**: {item.get('signature', 'unknown')} "
                f"({item.get('rationale', 'no rationale')})"
            )
        remaining = len(draft_tests) - max_items
        if remaining > 0:
            lines.append(f"- ... and {remaining} more")
        lines.append("")

    return lines


def _build_generate_gaps_section(report: Dict[str, Any], max_items: int = 6) -> List[str]:
    """Build gaps / action items section for generated suites."""
    gaps = report.get("gaps", [])
    if not gaps:
        return []

    lines = ["### Coverage Gaps", ""]
    for gap in gaps[:max_items]:
        lines.append(f"- \u26a0\ufe0f {gap}")
    remaining = len(gaps) - max_items
    if remaining > 0:
        lines.append(f"- ... and {remaining} more")
    lines.append("")
    return lines


def _build_generate_delta_section(report: Dict[str, Any], max_items: int = 5) -> List[str]:
    """Build changes-since-last-generation section."""
    delta = report.get("changes_since_last_generation")
    if not delta:
        return []

    lines = ["### Changes Since Last Generation", ""]
    tests_delta = delta.get("tests_generated_delta", 0)
    if tests_delta:
        sign = "+" if tests_delta > 0 else ""
        lines.append(f"- Draft tests: {sign}{tests_delta}")

    for label, key in (
        ("New behavior signatures", "new_signatures"),
        ("Resolved behavior signatures", "resolved_signatures"),
        ("New tools observed", "new_tools"),
        ("Resolved gaps", "resolved_gaps"),
        ("New gaps", "new_gaps"),
    ):
        values = delta.get(key) or []
        if not values:
            continue
        preview = ", ".join(values[:max_items])
        suffix = f", and {len(values) - max_items} more" if len(values) > max_items else ""
        lines.append(f"- {label}: {preview}{suffix}")

    lines.append("")
    return lines


# ── Comment generators ──────────────────────────────────────────────────


def generate_pr_comment(
    results: List[Dict[str, Any]],
    diff_results: Optional[List[Dict[str, Any]]] = None,
    run_url: Optional[str] = None,
) -> str:
    """Generate markdown PR comment from results."""
    if not results:
        return "## EvalView Results\n\nNo test results found."

    overall_status = _determine_overall_status(results, diff_results)
    status_display = get_status_display(overall_status)

    lines = []
    lines.append(f"## {status_display['emoji']} EvalView: {status_display['label']}")
    lines.append("")

    lines.extend(_build_summary_table(results))
    lines.append("")

    if diff_results:
        lines.extend(_build_changes_section(diff_results))
    else:
        lines.extend(_build_failed_tests_section(results))

    if run_url:
        lines.append(f"[View full report]({run_url})")
        lines.append("")

    lines.append("---")
    lines.append(COMMENT_SIGNATURE)

    return "\n".join(lines)


def _build_verdict_header(verdict_data: Dict[str, Any]) -> List[str]:
    """Build the verdict headline + reasons block.

    This is the first thing reviewers see — the one-line ship/don't-ship
    decision followed by up to 3 bulleted reasons. Designed to render
    cleanly in a PR comment and a Slack unfurl.
    """
    verdict_key = str(verdict_data.get("verdict", "safe_to_ship")).lower()
    cfg = VERDICT_CONFIG.get(verdict_key, VERDICT_CONFIG["safe_to_ship"])

    lines: List[str] = [
        f"## {cfg['emoji']} EvalView: {cfg['label']}",
        "",
    ]

    reasons = verdict_data.get("reasons") or []
    for reason in reasons[:4]:  # cap so we never dominate the comment
        lines.append(f"> {reason}")
    if reasons:
        lines.append("")
    return lines


def _build_verdict_signals_table(
    check_data: Dict[str, Any],
    verdict_data: Dict[str, Any],
) -> List[str]:
    """Build the Week-2 signals table: verdict, counts, drift, cost, quarantine.

    One row per signal so reviewers can scan it in two seconds.
    """
    summary = check_data.get("summary", {})
    total = summary.get("total_tests", 0)
    unchanged = summary.get("unchanged", 0)
    regressions = summary.get("regressions", 0)
    tools_changed = summary.get("tools_changed", 0)
    output_changed = summary.get("output_changed", 0)
    pass_rate = (unchanged / total * 100) if total > 0 else 0

    lines: List[str] = [
        "| Signal | Status |",
        "|--------|--------|",
        f"| Tests | {unchanged}/{total} unchanged ({pass_rate:.0f}%) |",
    ]
    if regressions:
        lines.append(f"| Regressions | \u274c {regressions} |")
    if tools_changed:
        lines.append(f"| Tools changed | \u26a0\ufe0f {tools_changed} |")
    if output_changed:
        lines.append(f"| Output changed | \u26a0\ufe0f {output_changed} |")

    # Cost delta (from Week 1 verdict payload — accurate aggregate ratio)
    cost_delta_ratio = verdict_data.get("cost_delta_ratio")
    if cost_delta_ratio is not None and abs(cost_delta_ratio) > 0.01:
        pct = int(round(cost_delta_ratio * 100))
        if pct > 0:
            lines.append(f"| Cost | \U0001f4b8 +{pct}% vs baseline |")
        else:
            lines.append(f"| Cost | \U0001f4b8 {pct}% vs baseline |")

    # Drift confidence
    drift_affected = verdict_data.get("drift_affected_tests", 0)
    if drift_affected:
        lines.append(
            f"| Model drift | \u26a0\ufe0f {drift_affected} test(s) trending down |"
        )

    # Quarantine health
    quarantine = verdict_data.get("quarantine") or {}
    q_total = quarantine.get("total", 0)
    q_stale = quarantine.get("stale", 0)
    if q_total:
        if q_stale:
            stale_tests = quarantine.get("stale_tests") or []
            # Escape backticks/pipes in test names so a malicious or
            # just-weird name can't break the table row.
            preview = ", ".join(
                f"`{_md_escape_inline(t)}`" for t in stale_tests[:2]
            )
            more = f" +{q_stale - 2}" if q_stale > 2 else ""
            lines.append(
                f"| Quarantine | {q_total} total, \u23f0 **{q_stale} stale** ({preview}{more}) |"
            )
        else:
            lines.append(f"| Quarantine | {q_total} active, all fresh |")

    # Model version change (existing detection, keep it)
    model_change = _detect_model_change(check_data)
    if model_change and model_change != "Yes":
        lines.append(f"| Model version | {model_change} |")

    # Behavioral anomalies (tool loops, stalls, brittle recovery)
    anom_data = verdict_data.get("behavioral_anomalies") or {}
    anom_count = anom_data.get("count", 0)
    if anom_count:
        anom_tests = anom_data.get("tests", [])[:3]
        preview = ", ".join(f"`{_md_escape_inline(t)}`" for t in anom_tests)
        lines.append(
            f"| Behavioral anomalies | \u26a0\ufe0f {anom_count} test(s): {preview} |"
        )

    # Low trust scores (benchmark gaming)
    trust_data = verdict_data.get("low_trust_tests") or {}
    trust_count = trust_data.get("count", 0)
    if trust_count:
        trust_tests = trust_data.get("tests", [])[:3]
        preview = ", ".join(f"`{_md_escape_inline(t)}`" for t in trust_tests)
        lines.append(
            f"| Low trust | \u26a0\ufe0f {trust_count} test(s): {preview} |"
        )

    # Coherence issues (multi-turn)
    coherence_data = verdict_data.get("coherence_issues") or {}
    coherence_count = coherence_data.get("count", 0)
    if coherence_count:
        coherence_tests = coherence_data.get("tests", [])[:3]
        preview = ", ".join(f"`{_md_escape_inline(t)}`" for t in coherence_tests)
        lines.append(
            f"| Coherence issues | \u26a0\ufe0f {coherence_count} multi-turn test(s): {preview} |"
        )

    return lines


def _build_recommendation_block(verdict_data: Dict[str, Any]) -> List[str]:
    """Render the top recommendation with its runnable command.

    One recommendation — the highest-ranked one — with likely cause and
    the first suggested command. Reviewers don't read three. They read
    one or none.
    """
    recs = verdict_data.get("recommendations") or []
    if not recs:
        return []

    top = recs[0]
    action = top.get("action", "")
    cause = top.get("likely_cause", "") or top.get("detail", "")
    conf = top.get("confidence", "medium")
    commands = top.get("suggested_commands") or []

    lines: List[str] = ["### Recommended action", ""]
    if cause:
        lines.append(f"**Likely cause ({conf} confidence):** {cause}")
        lines.append("")
    lines.append(f"**{action}**")
    if commands:
        preview = commands[:3]
        fence = _pick_code_fence(preview)
        lines.append("")
        lines.append(f"{fence}bash")
        for cmd in preview:
            lines.append(cmd)
        lines.append(fence)
    lines.append("")
    return lines


def generate_check_pr_comment(
    check_data: Dict[str, Any],
    run_url: Optional[str] = None,
) -> str:
    """Generate markdown PR comment from check --json output.

    Week 2 upgrade: when the check output carries a `verdict` key
    (from `_compute_verdict_payload`), the comment leads with the
    verdict headline, a signals table, and a recommended action block.
    Legacy format is kept as a fallback for pre-Week-1 check output.
    """
    verdict_data = check_data.get("verdict")
    if verdict_data:
        return _generate_verdict_first_comment(
            check_data, verdict_data, run_url=run_url
        )

    return _generate_legacy_check_comment(check_data, run_url=run_url)


def _generate_verdict_first_comment(
    check_data: Dict[str, Any],
    verdict_data: Dict[str, Any],
    run_url: Optional[str] = None,
) -> str:
    """Week-2 comment layout: verdict → signals → action → details."""
    lines: List[str] = []
    lines.extend(_build_verdict_header(verdict_data))
    lines.extend(_build_verdict_signals_table(check_data, verdict_data))
    lines.append("")
    lines.extend(_build_recommendation_block(verdict_data))
    lines.extend(_build_check_changes_section(check_data))

    if run_url:
        lines.append(f"[View full report]({run_url})")
        lines.append("")

    lines.append("---")
    lines.append(COMMENT_SIGNATURE)
    return "\n".join(lines)


def _generate_legacy_check_comment(
    check_data: Dict[str, Any],
    run_url: Optional[str] = None,
) -> str:
    """Pre-Week-2 comment format — used when verdict payload is absent."""
    summary = check_data.get("summary", {})
    regressions = summary.get("regressions", 0)
    tools_changed = summary.get("tools_changed", 0)
    output_changed = summary.get("output_changed", 0)

    if regressions > 0:
        overall_status = "regression"
    elif tools_changed > 0:
        overall_status = "tools_changed"
    elif output_changed > 0:
        overall_status = "output_changed"
    else:
        overall_status = "passed"

    status_display = get_status_display(overall_status)

    # Pre-compute model change once (used in both alerts and summary table)
    model_change = _detect_model_change(check_data)

    lines = []
    lines.append(f"## {status_display['emoji']} EvalView: {status_display['label']}")
    lines.append("")

    # Alerts block (cost/latency spikes, model changes)
    lines.extend(_build_alerts_section(check_data, model_change=model_change))

    lines.extend(_build_check_summary_table(check_data, model_change=model_change))
    lines.append("")

    lines.extend(_build_check_changes_section(check_data))

    if run_url:
        lines.append(f"[View full report]({run_url})")
        lines.append("")

    lines.append("---")
    lines.append(COMMENT_SIGNATURE)

    return "\n".join(lines)


