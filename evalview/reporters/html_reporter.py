"""HTML diff report generator (legacy diff-only reporter).

The main visual report is generated by evalview.visualization.generate_visual_report.
This module retains only DiffReporter for the --diff-report flag.
"""

import json
from collections import Counter
from datetime import datetime
from pathlib import Path
from typing import Any, Dict, List

try:
    from jinja2 import Environment, BaseLoader
    JINJA2_AVAILABLE = True
except ImportError:
    JINJA2_AVAILABLE = False

try:
    import plotly.graph_objects as go
    PLOTLY_AVAILABLE = True
except ImportError:
    PLOTLY_AVAILABLE = False

from evalview.core.types import EvaluationResult
from evalview.reporters._templates import HTML_TEMPLATE, DIFF_TEMPLATE  # noqa: F401


class _DeprecatedHTMLReporter:
    """Generate interactive HTML reports from evaluation results."""

    def __init__(self):
        if not JINJA2_AVAILABLE:
            raise ImportError(
                "jinja2 is required for HTML reports. Install with: pip install jinja2"
            )

    def generate(
        self,
        results: List[EvaluationResult],
        output_path: str,
        title: str = "EvalView Test Report",
    ) -> str:
        """
        Generate an HTML report from evaluation results.

        Args:
            results: List of evaluation results
            output_path: Path to write the HTML file
            title: Report title

        Returns:
            Path to the generated report
        """
        summary = self._compute_summary(results)
        charts = self._generate_charts(results) if PLOTLY_AVAILABLE else {}

        html = self._render_template(
            results=results,
            summary=summary,
            charts=charts,
            title=title,
            timestamp=datetime.now().isoformat(),
            plotly_available=PLOTLY_AVAILABLE,
        )

        output = Path(output_path)
        output.parent.mkdir(parents=True, exist_ok=True)
        output.write_text(html)

        return str(output)

    def _compute_summary(self, results: List[EvaluationResult]) -> Dict[str, Any]:
        """Compute summary statistics from results."""
        if not results:
            return {
                "total": 0,
                "passed": 0,
                "failed": 0,
                "pass_rate": 0,
                "avg_score": 0,
                "total_cost": 0,
                "total_latency": 0,
                "avg_latency": 0,
                "total_tokens": 0,
                "models": [],
                "models_display": "Unknown",
            }

        passed = sum(1 for r in results if r.passed)
        failed = len(results) - passed
        scores = [r.score for r in results]
        costs = [r.trace.metrics.total_cost for r in results]
        latencies = [r.trace.metrics.total_latency for r in results]
        total_tokens = sum(
            r.trace.metrics.total_tokens.total_tokens
            for r in results
            if r.trace.metrics.total_tokens
        )
        models = self._collect_models(results)

        return {
            "total": len(results),
            "passed": passed,
            "failed": failed,
            "pass_rate": round(passed / len(results) * 100, 1) if results else 0,
            "avg_score": round(sum(scores) / len(scores), 1) if scores else 0,
            "min_score": round(min(scores), 1) if scores else 0,
            "max_score": round(max(scores), 1) if scores else 0,
            "total_cost": round(sum(costs), 4),
            "avg_cost": round(sum(costs) / len(costs), 4) if costs else 0,
            "total_latency": round(sum(latencies), 0),
            "avg_latency": round(sum(latencies) / len(latencies), 0) if latencies else 0,
            "total_tokens": total_tokens,
            "models": models,
            "models_display": ", ".join(models) if models else "Unknown",
        }

    def _collect_models(self, results: List[EvaluationResult]) -> List[str]:
        """Collect model fingerprints used across results."""
        counts: Counter[str] = Counter()
        for result in results:
            for model_name in self._extract_models(result):
                counts[model_name] += 1
        return [model for model, _ in counts.most_common()]

    def _extract_models(self, result: EvaluationResult) -> List[str]:
        """Extract best-effort model labels from a result."""
        models: list[str] = []
        trace = result.trace
        if trace.model_id:
            if trace.model_provider:
                models.append(f"{trace.model_provider}/{trace.model_id}")
            else:
                models.append(trace.model_id)
        if trace.trace_context:
            for span in trace.trace_context.spans:
                if span.llm and span.llm.model:
                    provider = span.llm.provider or trace.model_provider
                    label = f"{provider}/{span.llm.model}" if provider else span.llm.model
                    models.append(label)
        # Preserve order while removing duplicates.
        return list(dict.fromkeys(models))

    def _extract_failure_reasons(self, r: EvaluationResult) -> List[str]:
        """Extract human-readable failure reasons from an evaluation result."""
        reasons: List[str] = []
        if not r.passed:
            # Contains check failures
            if r.evaluations.output_quality:
                cc = r.evaluations.output_quality.contains_checks
                if cc and cc.failed:
                    for phrase in cc.failed[:3]:
                        reasons.append(f"Missing required text: {phrase}")
                ncc = r.evaluations.output_quality.not_contains_checks
                if ncc and ncc.failed:
                    for phrase in ncc.failed[:3]:
                        reasons.append(f"Contains prohibited text: {phrase}")
            # Cost / latency threshold violations
            if not r.evaluations.cost.passed:
                reasons.append(f"Cost exceeded threshold: ${r.trace.metrics.total_cost:.4f}")
            if not r.evaluations.latency.passed:
                reasons.append(f"Latency exceeded threshold: {r.trace.metrics.total_latency:.0f}ms")
            # Tool accuracy
            if r.evaluations.tool_accuracy.accuracy < 1.0:
                missing = r.evaluations.tool_accuracy.missing
                if missing:
                    reasons.append(f"Missing tools: {', '.join(missing[:3])}")
            if not reasons:
                reasons.append(f"Score {r.score} below threshold")
        return reasons

    def _generate_charts(self, results: List[EvaluationResult]) -> Dict[str, str]:
        """Generate Plotly charts as JSON strings."""
        if not results:
            return {}

        charts = {}

        # Pass/Fail pie chart
        passed = sum(1 for r in results if r.passed)
        failed = len(results) - passed
        fig = go.Figure(data=[go.Pie(
            labels=["Passed", "Failed"],
            values=[passed, failed],
            marker_colors=["#22c55e", "#ef4444"],
            hole=0.4,
        )])
        fig.update_layout(
            title="Test Results",
            showlegend=True,
            margin=dict(t=40, b=20, l=20, r=20),
            height=300,
        )
        charts["pass_fail"] = fig.to_json()

        # Score distribution histogram
        scores = [r.score for r in results]
        fig = go.Figure(data=[go.Histogram(
            x=scores,
            nbinsx=10,
            marker_color="#3b82f6",
        )])
        fig.update_layout(
            title="Score Distribution",
            xaxis_title="Score",
            yaxis_title="Count",
            margin=dict(t=40, b=40, l=40, r=20),
            height=300,
        )
        charts["score_distribution"] = fig.to_json()

        # Cost breakdown bar chart
        test_names = [r.test_case[:20] + "..." if len(r.test_case) > 20 else r.test_case for r in results]
        costs = [r.trace.metrics.total_cost for r in results]
        fig = go.Figure(data=[go.Bar(
            x=test_names,
            y=costs,
            marker_color="#8b5cf6",
        )])
        fig.update_layout(
            title="Cost by Test",
            xaxis_title="Test",
            yaxis_title="Cost ($)",
            margin=dict(t=40, b=80, l=40, r=20),
            height=300,
            xaxis_tickangle=-45,
        )
        charts["cost_breakdown"] = fig.to_json()

        # Latency scatter plot
        latencies = [r.trace.metrics.total_latency for r in results]
        colors = ["#22c55e" if r.passed else "#ef4444" for r in results]
        fig = go.Figure(data=[go.Scatter(
            x=list(range(1, len(results) + 1)),
            y=latencies,
            mode="markers",
            marker=dict(size=12, color=colors),
            text=[r.test_case for r in results],
            hovertemplate="<b>%{text}</b><br>Latency: %{y:.0f}ms<extra></extra>",
        )])
        fig.update_layout(
            title="Latency by Test",
            xaxis_title="Test #",
            yaxis_title="Latency (ms)",
            margin=dict(t=40, b=40, l=40, r=20),
            height=300,
        )
        charts["latency_scatter"] = fig.to_json()

        # Evaluation breakdown stacked bar
        tool_acc = [r.evaluations.tool_accuracy.accuracy * 100 for r in results]
        output_qual = [r.evaluations.output_quality.score for r in results]
        seq_correct = [100 if r.evaluations.sequence_correctness.correct else 0 for r in results]

        fig = go.Figure()
        fig.add_trace(go.Bar(name="Tool Accuracy", x=test_names, y=tool_acc, marker_color="#22c55e"))
        fig.add_trace(go.Bar(name="Output Quality", x=test_names, y=output_qual, marker_color="#3b82f6"))
        fig.add_trace(go.Bar(name="Sequence", x=test_names, y=seq_correct, marker_color="#f59e0b"))
        fig.update_layout(
            title="Evaluation Breakdown",
            barmode="group",
            xaxis_tickangle=-45,
            margin=dict(t=40, b=80, l=40, r=20),
            height=350,
            legend=dict(orientation="h", yanchor="bottom", y=1.02, xanchor="right", x=1),
        )
        charts["eval_breakdown"] = fig.to_json()

        return charts

    def _serialize_spans(self, result: EvaluationResult) -> list:
        """Serialize span data for the trace replay timeline.

        Prefers the rich ``TraceContext`` (LLM + tool spans with full prompt/
        completion data). Falls back to the legacy ``StepTrace`` list when no
        ``trace_context`` is available so the replay works for every adapter.

        Args:
            result: Evaluation result whose trace we will serialise.

        Returns:
            A list of plain dicts, one per span, safe for JSON / Jinja2 rendering.
        """
        def _truncate(value, limit: int = 600) -> str:
            s = str(value) if value is not None else ""
            return s[:limit] + " …" if len(s) > limit else s

        def _json_preview(value, limit: int = 600) -> str:
            try:
                s = json.dumps(value, default=str, ensure_ascii=False, indent=2)
            except Exception:
                s = str(value)
            return s[:limit] + " …" if len(s) > limit else s

        trace = result.trace

        # ── Rich path: TraceContext is available ────────────────────────────
        if trace.trace_context and trace.trace_context.spans:
            spans = []
            for sp in sorted(trace.trace_context.spans, key=lambda s: s.start_time):
                entry: dict = {
                    "kind": sp.kind.value,         # "agent" | "llm" | "tool"
                    "name": sp.name,
                    "duration_ms": round(sp.duration_ms or 0, 1),
                    "status": sp.status,           # "ok" | "error" | "unset"
                    "cost": sp.cost,
                    "error": sp.error_message or "",
                    "llm": None,
                    "tool": None,
                }
                if sp.llm:
                    entry["llm"] = {
                        "model": sp.llm.model,
                        "provider": sp.llm.provider,
                        "prompt_tokens": sp.llm.prompt_tokens,
                        "completion_tokens": sp.llm.completion_tokens,
                        "finish_reason": sp.llm.finish_reason or "",
                        "prompt": _truncate(sp.llm.prompt),
                        "completion": _truncate(sp.llm.completion),
                    }
                if sp.tool:
                    entry["tool"] = {
                        "tool_name": sp.tool.tool_name,
                        "parameters": _json_preview(sp.tool.parameters),
                        "result": _truncate(sp.tool.result),
                    }
                spans.append(entry)
            return spans

        # ── Fallback path: convert StepTrace list ───────────────────────────
        return [
            {
                "kind": "tool",
                "name": step.tool_name,
                "duration_ms": round(step.metrics.latency, 1),
                "status": "ok" if step.success else "error",
                "cost": step.metrics.cost,
                "error": step.error or "",
                "llm": None,
                "tool": {
                    "tool_name": step.tool_name,
                    "parameters": _json_preview(step.parameters),
                    "result": _truncate(step.output),
                },
            }
            for step in trace.steps
        ]

    def _serialize_rationale_events(self, result: EvaluationResult) -> list:
        """Serialize rationale_events for the replay timeline.

        Schema v2 capture (docs: evalview/core/rationale.py). Missing
        field on older traces yields an empty list so templates can
        always iterate without guarding.
        """
        events = getattr(result.trace, "rationale_events", None) or []
        serialized = []
        for ev in events:
            text = ev.rationale_text or ""
            if len(text) > 600:
                text = text[:600] + " …"
            serialized.append({
                "step_id": ev.step_id,
                "turn": ev.turn if ev.turn is not None else "",
                "decision_type": ev.decision_type,
                "chosen": ev.chosen,
                "alternatives": list(ev.alternatives),
                "rationale_text": text,
                "truncated": ev.truncated,
                "confidence": (
                    round(ev.model_reported_confidence * 100)
                    if ev.model_reported_confidence is not None else None
                ),
            })
        return serialized

    def _render_template(
        self,
        results: List[EvaluationResult],
        summary: Dict[str, Any],
        charts: Dict[str, str],
        title: str,
        timestamp: str,
        plotly_available: bool,
    ) -> str:
        """Render the HTML template."""
        env = Environment(loader=BaseLoader())
        template = env.from_string(HTML_TEMPLATE)

        # Convert results to serializable format
        results_data = []
        for r in results:
            total_tokens = r.trace.metrics.total_tokens
            tokens_total = total_tokens.total_tokens if total_tokens else 0
            models = self._extract_models(r)
            results_data.append({
                "test_case": r.test_case,
                "passed": r.passed,
                "score": r.score,
                "input_query": r.input_query or "",
                "actual_output": (r.actual_output or "")[:2000],
                "tool_accuracy": round(r.evaluations.tool_accuracy.accuracy * 100, 1),
                "correct_tools": r.evaluations.tool_accuracy.correct,
                "missing_tools": r.evaluations.tool_accuracy.missing,
                "unexpected_tools": r.evaluations.tool_accuracy.unexpected,
                "output_quality": r.evaluations.output_quality.score,
                "output_rationale": r.evaluations.output_quality.rationale,
                "sequence_correct": r.evaluations.sequence_correctness.correct,
                "expected_sequence": r.evaluations.sequence_correctness.expected_sequence,
                "actual_sequence": r.evaluations.sequence_correctness.actual_sequence,
                "cost": round(r.trace.metrics.total_cost, 4),
                "latency": round(r.trace.metrics.total_latency, 0),
                "steps": len(r.trace.steps),
                "adapter": r.adapter_name or "http",
                "model": ", ".join(models) if models else "Unknown",
                "tokens_total": tokens_total,
                "tokens_input": total_tokens.input_tokens if total_tokens else 0,
                "tokens_output": total_tokens.output_tokens if total_tokens else 0,
                "tokens_cached": total_tokens.cached_tokens if total_tokens else 0,
                # Forbidden tool violations (empty list = no violations or not configured)
                "forbidden_violations": (
                    r.evaluations.forbidden_tools.violations
                    if r.evaluations.forbidden_tools and not r.evaluations.forbidden_tools.passed
                    else []
                ),
                # Hallucination, safety, PII — shown in CLI but was missing from HTML
                "hallucination_detected": (
                    r.evaluations.hallucination.detected
                    if r.evaluations.hallucination else False
                ),
                "hallucination_confidence": (
                    round(r.evaluations.hallucination.confidence * 100)
                    if r.evaluations.hallucination and r.evaluations.hallucination.detected else 0
                ),
                "safety_safe": (
                    r.evaluations.safety.safe
                    if r.evaluations.safety else True
                ),
                "safety_categories": (
                    r.evaluations.safety.flagged_categories
                    if r.evaluations.safety and not r.evaluations.safety.safe else []
                ),
                "pii_detected": (
                    r.evaluations.pii.detected
                    if r.evaluations.pii else False
                ),
                "pii_types": (
                    r.evaluations.pii.types_found
                    if r.evaluations.pii and r.evaluations.pii.detected else []
                ),
                # Failure reasons (from contains/not_contains checks + cost/latency)
                "failure_reasons": self._extract_failure_reasons(r),
                # Score threshold for context
                "min_score": r.min_score if hasattr(r, "min_score") else 0,
                # Trace replay: prefer rich TraceContext spans; fallback to StepTrace list
                "spans": self._serialize_spans(r),
                # Decision rationales captured by the adapter (schema v2).
                # Empty when the adapter doesn't support capture.
                "rationale_events": self._serialize_rationale_events(r),
            })

        compare_data = self._compute_compare(results_data)

        return template.render(
            title=title,
            timestamp=timestamp,
            summary=summary,
            results=results_data,
            charts=charts,
            plotly_available=plotly_available,
            compare=compare_data,
        )

    def _compute_compare(self, results_data: List[Dict[str, Any]]) -> Dict[str, Any]:
        """Build cross-model comparison data when multiple adapters ran the same tasks.

        Returns a dict with:
          - enabled: True only when ≥2 distinct adapters share ≥1 task name
          - models: ordered list of adapter labels
          - tasks: ordered list of task names
          - rows: {task_name: {adapter: {score, latency, tools, passed, cost}}}
        """
        from collections import defaultdict

        # Group by (task_name, adapter)
        by_task_adapter: Dict[str, Dict[str, Dict]] = defaultdict(dict)
        for r in results_data:
            task = r["test_case"]
            adapter = r["adapter"]
            by_task_adapter[task][adapter] = {
                "score": r["score"],
                "latency": r["latency"],
                "cost": r["cost"],
                "passed": r["passed"],
                "tool_accuracy": r["tool_accuracy"],
                "steps": r["steps"],
            }

        all_adapters: List[str] = list(dict.fromkeys(r["adapter"] for r in results_data))
        all_tasks: List[str] = list(dict.fromkeys(r["test_case"] for r in results_data))

        # Only show Compare tab when multiple adapters share at least one task name
        tasks_with_multiple = [
            t for t in all_tasks if len(by_task_adapter[t]) >= 2
        ]
        enabled = len(all_adapters) >= 2 and len(tasks_with_multiple) >= 1

        # Pre-compute per-model totals so Jinja2 doesn't need complex dict access
        totals: Dict[str, Dict[str, Any]] = {}
        for model in all_adapters:
            cells = [by_task_adapter[t][model] for t in all_tasks if model in by_task_adapter[t]]
            if cells:
                avg_score = round(sum(c["score"] for c in cells) / len(cells), 1)
                avg_lat = round(sum(c["latency"] for c in cells) / len(cells) / 1000, 1)
                total_cost = sum(c["cost"] for c in cells)
                cost_display = "free" if total_cost == 0.0 else f"${total_cost:.3f}"
            else:
                avg_score, avg_lat, cost_display = "—", "—", "—"
            totals[model] = {"avg_score": avg_score, "avg_latency": avg_lat, "cost_display": cost_display}

        return {
            "enabled": enabled,
            "models": all_adapters,
            "tasks": all_tasks,
            "rows": {task: by_task_adapter[task] for task in all_tasks},
            "totals": totals,
        }



class DiffReporter:
    """Generate HTML diff reports comparing actual vs golden traces."""

    def __init__(self):
        if not JINJA2_AVAILABLE:
            raise ImportError(
                "jinja2 is required for HTML reports. Install with: pip install jinja2"
            )

    def generate(
        self,
        diffs: list,  # List of TraceDiff objects
        results: List[EvaluationResult],
        output_path: str,
        title: str = "Golden Diff Report",
    ) -> str:
        """
        Generate an HTML diff report.

        Args:
            diffs: List of TraceDiff objects from diff engine
            results: List of EvaluationResult for additional context
            output_path: Path to write the HTML file
            title: Report title

        Returns:
            Path to the generated report
        """
        from evalview.core.golden import GoldenStore
        from evalview.core.diff import DiffStatus

        store = GoldenStore()

        # Build diff data for template
        diff_data = []
        regressions = 0
        tools_changed = 0
        output_changed = 0
        passed = 0

        for trace_diff in diffs:
            # Get corresponding result
            result = next((r for r in results if r.test_case == trace_diff.test_name), None)
            golden = store.load_golden(trace_diff.test_name)

            if trace_diff.overall_severity == DiffStatus.REGRESSION:
                status = "regression"
                regressions += 1
            elif trace_diff.overall_severity == DiffStatus.TOOLS_CHANGED:
                status = "tools_changed"
                tools_changed += 1
            elif trace_diff.overall_severity == DiffStatus.OUTPUT_CHANGED:
                status = "output_changed"
                output_changed += 1
            else:
                status = "passed"
                passed += 1

            # Build tool changes list
            tool_changes = []
            for td in trace_diff.tool_diffs:
                if td.type == "added":
                    tool_changes.append({"type": "added", "tool": td.actual_tool})
                elif td.type == "removed":
                    tool_changes.append({"type": "removed", "tool": td.golden_tool})
                elif td.type == "changed":
                    tool_changes.append({"type": "changed", "from": td.golden_tool, "to": td.actual_tool})

            diff_data.append({
                "test_name": trace_diff.test_name,
                "status": status,
                "score_diff": trace_diff.score_diff,
                "actual_score": result.score if result else 0,
                "similarity": trace_diff.output_diff.similarity if trace_diff.output_diff else 1.0,
                "golden_tools": golden.tool_sequence if golden else [],
                "actual_tools": [s.tool_name for s in result.trace.steps] if result else [],
                "tool_changes": tool_changes,
                "golden_output": golden.trace.final_output[:1000] if golden else "",
                "actual_output": result.trace.final_output[:1000] if result else "",
            })

        # Render template
        env = Environment(loader=BaseLoader())
        template = env.from_string(DIFF_TEMPLATE)
        html = template.render(
            title=title,
            timestamp=datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
            summary={
                "total": len(diffs),
                "regressions": regressions,
                "tools_changed": tools_changed,
                "output_changed": output_changed,
                "passed": passed,
            },
            diffs=diff_data,
        )

        output = Path(output_path)
        output.parent.mkdir(parents=True, exist_ok=True)
        output.write_text(html)

        return str(output)


